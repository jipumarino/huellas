<div class="entry">
<h3>¿Qué es el Eneagrama?</h3>
<p>
Es una poderosa herramienta de autoconocimiento y crecimiento personal desarrollado hace más de dos mil años por la cultura Sufí, la que a través de la introspección y la aceptación total de sí mismo ayuda a ir superando trabas personales e interpersonales, mejorando así sustantivamente la relación con uno mismo, con la familia, amigos e incluso con Dios.
</p>
<p>
En ella se describen los nueve tipos básicos de personalidad presentes en la humanidad, mostrando sus principales conflictos y debilidades, dando pistas para despertar nuestro ser esencial que está lleno de virtudes y  potencialidades
</p>
<h3>¿Para qué sirve?</h3>
<p>
Si aceptamos el hecho de que sólo puede amarse lo que se conoce, entonces necesariamente aceptaremos también que mientras más y mejor nos conozcamos, mayores posibilidades tendremos para querernos a nosotros mismos. Por otra parte, a mostrar el funcionamiento básico de todas las personalidades, también podremos conocer mejor (y por lo tanto amar más) a quienes nos rodean.
</p>
<p>
En términos amplios, puede afirmarse que el Eneagrama aspira al aprendizaje, dándonos estrategias y herramientas para ser más concientes de sí e ir en un camino creciente de conocimiento: personal, de los demás, de Dios, lo que ayuda a estar en paz consigo mismos y vincularse mejor con los demás, considerando que el amor no aspira a la perfección, sino que a la liberación de nuestros temores, a la gratuidad en los vínculos, la aceptación, cariño incondicional y la verdad.
</p>

